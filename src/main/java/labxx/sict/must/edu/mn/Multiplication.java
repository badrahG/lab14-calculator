package labxx.sict.must.edu.mn;

/**
 * Үржүүлэх үйлдэл хийдэг класс.
 */
public class Multiplication {
    
    /**
     * Хоёр тоог үржүүлнэ.
     *
     * @param a эхний тоо
     * @param b хоёр дахь тоо
     * @return үржвэр
     */
    public double multiply(double a, double b) {
        return a * b;
    }
}